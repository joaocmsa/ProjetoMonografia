    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:
        String data = "  /  /    ";
        if(jFormattedTextDataFinal.getText().equals(data) || jFormattedTextDataInicial.getText().equals(data)){
            if(jFormattedTextDataInicial.getText().equals(data)){
                JOptionPane.showMessageDialog(null, "Digite a data inicial da busca!");
                jFormattedTextDataInicial.grabFocus();
            }else{
                JOptionPane.showMessageDialog(null, "Digite a data final da busca!");
                jFormattedTextDataFinal.grabFocus();
            }
        }else{
            
            relatorio.atualiza();
            jTableVendas.setModel(relatorio);
            TabelaVenda(jFormattedTextDataInicial.getText(), jFormattedTextDataFinal.getText());
            
            JOptionPane.showMessageDialog(null, "Busca realizada com sucesso!");
        
        }
        
    }//GEN-LAST:event_jButtonBuscarActionPerformed

    private void jTableVendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableVendasMouseClicked
        // TODO add your handling code here:
        if(jTableVendas.getSelectedRow() != -1){
            
            long cod = Long.parseLong(jTableVendas.getValueAt(jTableVendas.getSelectedRow(), 0).toString());     
            daovenda.setPesquisa(cod);
            itens.atualiza();
            jTableProdutos.setModel(itens);
            TabelaItens();
        }   
        
    }//GEN-LAST:event_jTableVendasMouseClicked


