    private void jTextFieldCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldCodigoFocusLost
        // TODO add your handling code here:
        if (jTextFieldCodigo.getText().isEmpty()) {
        } else {
            daoestoque.setPesquisa(Long.parseLong(jTextFieldCodigo.getText()));
            daoestoque.BuscaEstoque(estoque);
            daoproduto.setCods(estoque.getProduto_Cod_Prod());
            daoproduto.buscaProdutoCod(produto);
            jTextFieldCodigo.setText(String.valueOf(estoque.getProduto_Cod_Prod()));
            jTextFieldProduto.setText(String.valueOf(produto.getNome_Prod()));
            jTextFieldQuantAtual.setText(String.valueOf(estoque.getQuantidade()));
        }
    }//GEN-LAST:event_jTextFieldCodigoFocusLost

