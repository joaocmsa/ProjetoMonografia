    private void jTableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutosMouseClicked
        // TODO add your handling code here:
        if (jTableProdutos.getSelectedRow() != -1) {

            jTextFieldProduto.setText((String) jTableProdutos.getValueAt(jTableProdutos.getSelectedRow(), 1));
            long qu = (long) jTableProdutos.getValueAt(jTableProdutos.getSelectedRow(), 2);
            long cod = (long) jTableProdutos.getValueAt(jTableProdutos.getSelectedRow(), 0);
            jTextFieldCodigo.setText(String.valueOf(cod));
            String quant = String.valueOf(qu);
            jTextFieldQuantAtual.setText(quant);

        }
    }//GEN-LAST:event_jTableProdutosMouseClicked

