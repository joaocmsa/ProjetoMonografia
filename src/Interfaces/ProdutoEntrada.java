    private void jButtonPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisaActionPerformed
        // TODO add your handling code here:
        if (jTextFieldProduto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Para realizar uma pesquisa digite o nome do produto.");
        } else {
            daoestoque.setPesquisar(jTextFieldProduto.getText());
            modelo.atualiza();
            jTableProdutos.setModel(modelo);
            Tabela2();
        }
    }//GEN-LAST:event_jButtonPesquisaActionPerformed

    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        // TODO add your handling code here:
        if (jTextFieldProduto.getText().isEmpty() || jTextFieldQuantidade.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Os campos não podem estar em branco!");
        } else {
            try {
                //faz a troca do nome do produto pelo codigo do produto
                con = new ConexaoBanco().Conecta();
                PreparedStatement stmt = con.prepareStatement("SELECT * FROM produto where Nome_Prod = ?");
                stmt.setString(1, jTextFieldProduto.getText());
                ResultSet resultado = stmt.executeQuery();
                resultado.first();
                Long codigo = resultado.getLong("Cod_Prod");
                long quantidade = Long.parseLong(jTextFieldQuantidade.getText());
                stmt.close();
                resultado.close();

                estoque.setQuantidade(quantidade);
                //realiza a ação de adicionar no estoque
                EstoqueDAO entrada = new EstoqueDAO();
                entrada.setPesquisa(codigo);
                entrada.EntradaProduto(estoque);
                JOptionPane.showMessageDialog(null, "Entrada de produto realizada com sucesso!");

                jTextFieldProduto.setText("");
                jTextFieldQuantidade.setText("");

                modelo.atualiza();
                jTableProdutos.setModel(modelo);
                Tabela();

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ProdutoEntrada.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(ProdutoEntrada.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

