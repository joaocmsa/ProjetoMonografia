    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed
        // TODO add your handling code here:
        if (jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite o nome do fabricante!");
        } else {
            fab.setNome_Fabr(jTextFieldNome.getText());

            dao.AddFab(fab);
            JOptionPane.showMessageDialog(null, "Fabricante: " + jTextFieldNome.getText() + " adicionado com sucesso!");
            tabela.atualiza();
            jTableFab.setModel(tabela);
            Tabela();
            jTextFieldNome.setText("");
        }
    }//GEN-LAST:event_jButtonGravarActionPerformed

    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:
        if (jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Para realizar uma busca com a tabela, digite o nome do fabricante!");
        } else {
            tabela.atualiza();
            jTableFab.setModel(tabela);
            Tabela2(jTextFieldNome.getText());
            jTextFieldNome.setText("");
            
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        // TODO add your handling code here:
        if (jTextFieldCodigo.getText().isEmpty() || jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campos nÃ£o preenchidos!");
        } else {
            long cod = Long.parseLong(jTextFieldCodigo.getText());

            fab.setNome_Fabr(jTextFieldNome.getText());
            dao.alteraFab(fab, cod);
            tabela.atualiza();
            jTableFab.setModel(tabela);
            Tabela();
            jTextFieldNome.setText("");
            jTextFieldCodigo.setText("");
            jButtonAlterar.setEnabled(false);
            jButtonGravar.setEnabled(true);

            JOptionPane.showMessageDialog(null, "Cadastro alterado com sucesso!");

            jButtonAlterar.setEnabled(!true);
            jButtonGravar.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    private void jTableFabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableFabMouseClicked
        // TODO add your handling code here:
        if (jTableFab.getSelectedRow() != -1) {
            jTextFieldNome.setText(jTableFab.getValueAt(jTableFab.getSelectedRow(), 1).toString());
            jTextFieldCodigo.setText(jTableFab.getValueAt(jTableFab.getSelectedRow(), 0).toString());
            jButtonAlterar.setEnabled(true);
            jButtonGravar.setEnabled(false);
        }
    }//GEN-LAST:event_jTableFabMouseClicked

